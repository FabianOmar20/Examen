<h2>Sistemas de conexión libre y a través de redes</h2>
<p>
    Conexión libre
    <br>
    Estas computadoras de escritorio se conectan a las computadoras de servicio empleando software que permite la emulación de algún tipo de terminal. En otros de los casos se les transfiere la información haciendo uso de recursos magnéticos o por trascripción.
    <br>
    Las redes son conjuntos de ordenadores independientes que se comunican a través de un medio compartido. Las LAN conectan una serie de ordenadores confinados en un área geográfica, como un edificio o un campus. Pueden unir muchos centenares de ordenadores y pueden ser usadas por muchos miles de usuarios.
